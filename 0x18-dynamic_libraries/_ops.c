/**
 * add - adds two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * add - subtracts two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: difference between a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * add - multiplies two numbers
 *
 * @a: first number
 * @b: second number
 * Return: product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * add - divides two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: a divided by b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * add - finds the modulus of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: modulus of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
